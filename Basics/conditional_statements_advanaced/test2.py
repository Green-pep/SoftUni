import os

# Prompt the user for their name
names = input("")

# Sanitize the input name to remove invalid characters for a filename
sanitized_names = ''.join(char for char in names if char.isalnum() or char in (' ', '_', '-'))

# Get the user's desktop directory
desktop_path = os.path.join(os.path.expanduser("~"), "Desktop")

# Create the file path on the desktop
file_path = os.path.join(desktop_path, f"{sanitized_names}.txt")

# Open and write to the file with UTF-8 encoding
with open(file_path, "w", encoding="utf-8") as f:
    f.write(f'''Уважаеми г-н/г-жо {names},
        Искаме да ви уведомим, че кандидатурата ви за бърз заем не може да бъде обработена поради неправилния формат на изпратените документи. Конкретно, снимките, изпратени като лични документи, включително селфитата с двете страни на личната карта, не са в правилния формат и не могат да бъдат приети за обработка.

        Ние разбираме важността на правилното представяне на личните документи при кандидатстване за финансови услуги и се стремим да осигурим сигурност и точност в целия процес.

        Ако желаете да подадете нова кандидатура, моля, обърнете внимание на следните стъпки:

        Уверете се, че снимките на документите са четливи и в правилния формат (например, JPEG или PDF).
        Отново изпратете снимките като прикачени файлове към този имейл.
        Ако предпочитате, можете да кандидатствате отново чрез нашия уебсайт, като следвате указанията за качване на личните документи в правилния формат.

        Извиняваме се за причиненото неудобство и се надяваме, че ще имаме възможност да обслужим вашите финансови нужди след като предоставите коректните документи.

С уважение,
Александра Иванова
Фаст Кеш Солюшън Българя
Клиентско Обслужване
''')

print(f"The file {sanitized_names}.txt has been created on your desktop.")
